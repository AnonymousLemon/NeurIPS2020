function [x,hist] = minFunc(objFun,x0,args,method)

assert( strcmp(method, 'newton-cg') || ...
    strcmp(method, 'gauss-newton') || ...
    strcmp(method,'newton-mr') || ...
    strcmp(method,'trust-region') || ...
    strcmp(method, 'lbfgs') || ...
    strcmp(method, 'nonlinear-cg') || ...
    strcmp(method, 'gd') || ...
    strcmp(method, 'acc_gd') ||...
    strcmp(method, 'adam') || ...
    strcmp(method, 'adadelta'));

if strcmp(method, 'newton-cg') || strcmp(method, 'gauss-newton')
    [x,hist] = optNewtonCG(objFun,x0,args, method);
end
if strcmp(method, 'newton-mr')
    [x,hist] = optNewtonMR(objFun,x0,args);
end
if strcmp(method, 'trust-region')
    [x,hist] = optTrustRegion(objFun,x0,args);
end
if strcmp(method, 'lbfgs')
    [x,hist] = optLBFGS(objFun,x0,args);
end
if strcmp(method, 'nonlinear-cg')
    [x,hist] = optNonlinearCG(objFun,x0,args);
end
if ( strcmp(method, 'gd') || ...
        strcmp(method, 'acc_gd') ||...
        strcmp(method, 'adam') || ...
        strcmp(method, 'adadelta') )
    [x,hist] = optFirstOrderMethods(objFun,x0,args, method);
end
end