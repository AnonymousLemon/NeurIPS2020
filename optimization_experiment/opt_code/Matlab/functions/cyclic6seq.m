function [x] = cyclic6seq(phi,n)
J = floor(n/6);
I = mod(n, 6);
x = zeros(n,2);

for j = 1:J
    x(6*j - 5, :) = [-87.5, -229*phi^(2*j - 2)/44];
    x(6*j - 4, :) = [-73.5, 387*phi^(2*j - 2)/44];
    x(6*j - 3, :) = [86.5, -53*phi^(2*j - 2)/44];
    x(6*j - 2, :) = [87.5, -229*phi^(2*j - 1)/44];
    x(6*j - 1, :) = [73.5, 387*phi^(2*j - 1)/44];
    x(6*j, :) = [-86.5, -53*phi^(2*j - 1)/44];
end
if I > 0
    x(6*J + 1, :) = [-87.5, -229*phi^(2*J)/44];
    if I > 1
        x(6*J + 2, :) = [-73.5, 387*phi^(2*J)/44];
        if I > 2
            x(6*J + 3, :) = [86.5, -53*phi^(2*J)/44];
            if I > 3
                x(6*J + 4, :) = [87.5, -229*phi^(2*J + 1)/44];
                if I > 4
                    x(6*J + 5, :) = [73.5, 387*phi^(2*J + 1)/44];
                end
            end
        end
    end
end    

end